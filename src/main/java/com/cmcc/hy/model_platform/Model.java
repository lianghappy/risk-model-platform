package com.cmcc.hy.model_platform;






public class Model {
    private String year;
    private String month;
    private String day;
    private String hour;
    private String sdk_version;
    private String mobile_os;
    private String ip_region;

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public String getHour() {
        return hour;
    }

    public void setHour(String hour) {
        this.hour = hour;
    }

    public String getSdk_version() {
        return sdk_version;
    }

    public void setSdk_version(String sdk_version) {
        this.sdk_version = sdk_version;
    }

    public String getMobile_os() {
        return mobile_os;
    }

    public void setMobile_os(String mobile_os) {
        this.mobile_os = mobile_os;
    }

    public String getIp_region() {
        return ip_region;
    }

    public void setIp_region(String ip_region) {
        this.ip_region = ip_region;
    }
}
